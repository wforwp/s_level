-- Result event storage for Dirty Mind Test
-- Run this in Supabase SQL Editor.

create table if not exists public.result_events (
  id bigint generated by default as identity primary key,
  session_id uuid not null,
  nickname text not null,
  age integer not null check (age between 19 and 99),
  total_score integer not null check (total_score between 0 and 274),
  level_code text not null,
  level_title text not null,
  level_summary text not null,
  locale text not null,
  answers jsonb not null default '{}'::jsonb,
  captured_at timestamptz not null,
  created_at timestamptz not null default now()
);

create index if not exists result_events_created_at_idx
  on public.result_events (created_at desc);

create index if not exists result_events_level_code_idx
  on public.result_events (level_code);

create index if not exists result_events_locale_idx
  on public.result_events (locale);

create unique index if not exists result_events_session_id_uidx
  on public.result_events (session_id);

alter table public.result_events enable row level security;

-- Allow anonymous inserts from client-side event emitter.
drop policy if exists "result_events_insert_anon" on public.result_events;
create policy "result_events_insert_anon"
  on public.result_events
  for insert
  to anon
  with check (true);

-- Block all selects/updates/deletes for anon users.
drop policy if exists "result_events_no_select_anon" on public.result_events;
create policy "result_events_no_select_anon"
  on public.result_events
  for select
  to anon
  using (false);

drop policy if exists "result_events_no_update_anon" on public.result_events;
create policy "result_events_no_update_anon"
  on public.result_events
  for update
  to anon
  using (false)
  with check (false);

drop policy if exists "result_events_no_delete_anon" on public.result_events;
create policy "result_events_no_delete_anon"
  on public.result_events
  for delete
  to anon
  using (false);
